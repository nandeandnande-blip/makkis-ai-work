function foodImage(id: string, category: FoodCategory): string {
  const emoji = FOOD_EMOJIS[id] ?? '🍽️';
  const bg = CATEGORY_COLORS[category];
  const svg = `
    <svg xmlns="http://www.w3.org/2000/svg" width="80" height="80" viewBox="0 0 80 80">
      <rect width="80" height="80" rx="16" fill="${bg}"/>
      <text x="40" y="54" font-size="36" text-anchor="middle">${emoji}</text>
    </svg>
  `.trim();
  return `data:image/svg+xml;base64,${btoa(unescape(encodeURIComponent(svg)))}`;
}

